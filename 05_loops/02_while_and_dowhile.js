// while loop
let index = 0;
while (index <= 10) {
    // console.log(`Value of index is: ${index}`);
    index = index + 2;
}

const myArr = [11, 22, 33, 44, 55];

let i = 0;
while(i < myArr.length){
    // console.log(`Value at index ${i} is ${myArr[i]}`);
    i++;
}

// do while loop
let score = 11;
do {
    console.log(`Score is ${score}`);
    score++;
} while (score <= 10);